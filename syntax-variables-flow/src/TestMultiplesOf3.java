
public class TestMultiplesOf3 {
	public static void main(String args[]) {
		for (int i = 3; i < 100; i += 3) {
			System.out.print(i + " ");
		}
			System.out.println("");

		for (int number = 1; number <= 100; number++) {
			if (number % 3 == 0) {
				System.out.print(number + " ");
				}
			System.out.print("");
		}

	}
}
